from_string(<<"more">>)           -> {ok, 16#29};
from_string(<<"error">>)          -> {ok, 16#2A};
from_string(<<"code">>)           -> {ok, 16#2B};
from_string(<<"detail">>)         -> {ok, 16#2C};
from_string(<<"min_version">>)    -> {ok, 16#2D};
from_string(<<"max_version">>)    -> {ok, 16#2E};
from_string(_)                    -> not_found.
